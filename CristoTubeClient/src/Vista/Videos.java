    public void mostrar(String cadena){   
        final SwingWorker worker = new SwingWorker() {
            @Override
            protected Object doInBackground() throws Exception {
                Login.controller.mostrarVideos(textAreaDebug, cadena, listaVideos);
                return null;
            }
        };
        worker.execute();
    }
    
    public void verVideo(){
        Login.terminarRecibir = false;
        String idVideo = (String) listaVideos.getModel().getValueAt(listaVideos.getSelectedRow(), 1);
        String login = (String) listaVideos.getModel().getValueAt(listaVideos.getSelectedRow(), 0);
        String nombreVideo = (String) listaVideos.getModel().getValueAt(listaVideos.getSelectedRow(), 2);
        String cadena = "PROTOCOLCRISTOTUBE1.0#" + login + "#GETVIDEO#" + idVideo;
        try {
            Login.controller.conectar(textAreaDebug, cadena);
        } catch (IOException ex) {
            System.out.println(ex);
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }

